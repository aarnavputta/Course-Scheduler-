    private void dropStudentButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dropStudentButtonActionPerformed
//        // TODO add your handling code here:
        String studentName=dropStudentCB.getSelectedItem().toString();
        String studentID=((StudentEntry)(dropStudentCB.getSelectedItem())).getStudentID();
        if (studentID.length() > 0) {
            StudentQueries.dropStudent(studentID);
            dropStudentTA.append(studentName+" has been dropped from the list \n\n");
            ArrayList<String> semList = SemesterQueries.getSemesterList();
            for(String s: semList){
                dropStudentTA.append("\n\nFor Semester: "+s+"\n");
                if(ScheduleQueries.getScheduleByStudent(s, studentID).size()>0){
            
                ArrayList<ScheduleEntry> studentClassList = ScheduleQueries.getScheduleByStudent(s, studentID);
                for (ScheduleEntry c: studentClassList){
                    String currCourse = c.getCourseCode();
                    ArrayList<ScheduleEntry> waitlistedStudentList = ScheduleQueries.getWaitlistedStudentsByClass(s,currCourse);
                    dropStudentTA.append(studentName+" has been dropped from the course "+c.getCourseCode()+"\n");
                    ScheduleQueries.dropStudentScheduleByCourse(s, studentID, currCourse);
                    if(waitlistedStudentList.size()!=0)
                    {
                        ScheduleQueries.updateScheduleEntry(s, waitlistedStudentList.get(0));
                        dropStudentTA.append(StudentQueries.getStudent(waitlistedStudentList.get(0).getStudentID())+" is scheduled into the course"+waitlistedStudentList.get(0).getCourseCode()+"\n");
                    }
                    
                }
            }
        }}
        rebuildStudentComboBox();
        
        
    }//GEN-LAST:event_dropStudentButtonActionPerformed

