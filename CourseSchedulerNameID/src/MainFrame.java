    private void scheduleClassbuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_scheduleClassbuttonActionPerformed
        // TODO add your handling code here:
        String courseSelected = selectClassComboBox.getSelectedItem().toString();
        String studentSelectedName = studentComboBox.getSelectedItem().toString();
        int seatsReserve = ScheduleQueries.getScheduledStudentCount(currentSemester, courseSelected);
        int possibleSeats = ClassQueries.getClassSeats(currentSemester, courseSelected);
        
        ArrayList<StudentEntry> students = StudentQueries.getAllStudents();
        String selectedStudentID = students.get(studentComboBox.getSelectedIndex()).getStudentID();
        
        java.sql.Timestamp currentTimestamp = new java.sql.Timestamp(Calendar.getInstance().getTime().getTime());
        
        if(seatsReserve<possibleSeats){
            ScheduleEntry student = new ScheduleEntry(currentSemester,courseSelected,selectedStudentID,"s",currentTimestamp);
            ScheduleQueries.addScheduleEntry(student);
            scheduleCoursesLabel.setText(studentSelectedName+" has been scheduled for the class");
            
        }
        else{
            ScheduleEntry student = new ScheduleEntry(currentSemester,courseSelected,selectedStudentID,"w",currentTimestamp);
            ScheduleQueries.addScheduleEntry(student);
            scheduleCoursesLabel.setText(studentSelectedName+" has been waitlisted for the class");
            
        
        }
        rebuildClassComboBox();
        rebuildStudentComboBox();
    }//GEN-LAST:event_scheduleClassbuttonActionPerformed

